// Operadores aritméticos básicos em JavaScript: + - * / % **

// Declarando duas variáveis numéricas
let n1 = 10
let n2 = 5

// Operação de soma
let soma = n1 + n2    // 10 + 5 = 15

// Operação de subtração
let subtração = n1 - n2   // 10 - 5 = 5

// Operação de multiplicação
let multiplicação = n1 * n2   // 10 * 5 = 50

// Operação de divisão
let divisão = n1 / n2     // 10 / 5 = 2

// Operação de módulo (resto da divisão inteira)
let restoDivisão = n1 % n2   // 10 % 5 = 0, pois 10 é divisível por 5 sem resto

// Operação de exponenciação
let potenciação = n1 ** n2   // 10 ** 5 = 100000 (10 elevado à 5ª potência)

// Exibindo resultados no console
console.log("Operação entre 10 e 5")
console.log(`Soma: ${soma}`)
console.log(`Subtração: ${subtração}`)
console.log(`Multiplicação: ${multiplicação}`)
console.log(`Divisão: ${divisão}`)
console.log(`Resto da Divisão: ${restoDivisão}`)
console.log(`Potenciação: ${potenciação}`)
