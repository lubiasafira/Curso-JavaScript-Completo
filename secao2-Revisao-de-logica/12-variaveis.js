// Atribuição de variáveis:
// Usamos "let" para declarar variáveis que podem ter o valor alterado.
// Aqui estamos atribuindo o número 5 para "a".
let a = 5

// De forma semelhante, atribuímos o número 2 para "b".
let b = 2

// Operações aritméticas:
// Podemos usar as variáveis em expressões matemáticas.
// O operador "+" faz a soma de a e b (5 + 2).
// O resultado é interpolado (inserido) dentro da string com o uso de template string (` `).
console.log(`A soma de a e b é: ${a + b}`)
