// ======================================
// Operadores de Atribuição em JavaScript
// ======================================

// Declarando uma variável
let n = 10   // atribuição simples (=)

// Operador +=  → soma e atribui
n += 5       // equivale a: n = n + 5
console.log("Após n += 5 →", n)   // 15

// Operador -=  → subtrai e atribui
n -= 3       // equivale a: n = n - 3
console.log("Após n -= 3 →", n)   // 12

// Operador *=  → multiplica e atribui
n *= 2       // equivale a: n = n * 2
console.log("Após n *= 2 →", n)   // 24

// Operador /=  → divide e atribui
n /= 4       // equivale a: n = n / 4
console.log("Após n /= 4 →", n)   // 6

// Operador %=  → calcula o resto e atribui
n %= 5       // equivale a: n = n % 5
console.log("Após n %= 5 →", n)   // 1

// Operador **=  → exponencia e atribui
n **= 3      // equivale a: n = n ** 3
console.log("Após n **= 3 →", n)  // 1 ** 3 = 1
